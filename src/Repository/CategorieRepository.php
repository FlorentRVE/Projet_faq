    public function getCategoriesViaUserDepartement($user)
    {
        return $this->createQueryBuilder('c')

            ->innerJoin('c.departement', 'd')
            ->innerJoin('d.users', 'u')
            ->andWhere('u.email = :users')
            ->setParameter('users', $user);
    }

